CREATE DATABASE train_schedule;
USE train_schedule;
CREATE TABLE stations
(
    id           INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    station_name VARCHAR(191) NOT NULL
);
CREATE TABLE weekdays
(
    id           TINYINT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    weekday_name VARCHAR(191) NOT NULL
);
CREATE TABLE schedules
(
    id             INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    stations_id    INT UNSIGNED     NOT NULL,
    weekdays_id    TINYINT UNSIGNED NOT NULL,
    departure_time TIME             NOT NULL,
    FOREIGN KEY (stations_id) REFERENCES stations (id),
    FOREIGN KEY (weekdays_id) REFERENCES weekdays (id)
);
